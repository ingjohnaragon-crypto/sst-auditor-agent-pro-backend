import unittest
from .. import cron


class CronTest(unittest.TestCase):
    def test_cron_validator_valid(self):
        # Note that the following checks for validity on the regex parsing level. It doesn't
        # guarantee that the resulting expression will be accepted during actual schedule creation.
        # For example, this doesn't validate whether the expression refers to a time in the past.
        expressions = [
            "* * * * * * *",
            "? ? ? ? ? ? ?",
            "0,59 * * * * * *",
            "00,02,59 01 02 03 04 05 *",
            "0-59/5 * * * * * *",
            "0/5 * * * * * *",
            "0/5,0-5/2,0-1 * * * * * *",
            "* 0,59 * * * * *",
            "* 0-59/5 * * * * *",
            "* 0/5 * * * * *",
            "* 0/5,0-5/2,0-1 * * * * *",
            "* * 0,23 * * * *",
            "* * 0-23/5 * * * *",
            "* * 0/5 * * * *",
            "* * 0/5,0-5/2,0-1 * * * *",
            "* * * L * * *",
            "* * * LW * * *",
            "* * * 12W * * *",
            "* * * 1,12,L * * *",
            "* * * 1,31 * * *",
            "* * * 1-23/5 * * *",
            "* * * 1/5 * * *",
            "* * * 1/5,1-5/2,1-1 * * *",
            "* * * last * * *",
            "* * * * JAN * *",
            "* * * * feb * *",
            "* * * * mar * *",
            "* * * * Apr * *",
            "* * * * aPr * *",
            "* * * * MAY * *",
            "* * * * JuN * *",
            "* * * * jUL * *",
            "* * * * auG * *",
            "* * * * SEP * *",
            "* * * * OCT-NOV * *",
            "* * * * DEC/2 * *",
            "* * * * 1-11/5 * *",
            "* * * * 1/5 * *",
            "* * * * 1/5,1-5/2,1-1 * *",
            "* * * * * MON *",
            "* * * * * SUN *",
            "* * * * * TUE-FRI *",
            "* * * * * SUN *",
            "* * * * * 0-6 *",
            "* * * * * 1-2/56 *",
            "* * * * * 5#2 *",
            "* * * * * TUE#2 *",
            "* * * * * * 1970",
            "* * * * * * 2099",
            "* * * * * * 1990-2000",
            "* * * * * * 1990-2000/2",
            "* * * * * * 1990-2000/2,2020",
        ]

        for expression in expressions:
            result = cron.validate_cron(expression)
            self.assertTrue(result, expression)

    def test_cron_validator_invalid(self):
        expressions = [
            "0/0/0 * * * * * *",
            "0/0/0,0-0 * * * * * *",
            "60 * * * * * *",
            "-0 * * * * * *",
            "-1 * * * * * *",
            "001 * * * * * *",
            "1L * * * * * *",
            "L * * * * * *",
            "W * * * * * *",
            "LW * * * * * *",
            "* 0/0/0 * * * * *",
            "* 0/0/0,0-0 * * * * *",
            "* 001 * * * * *",
            "* 60 * * * * *",
            "* -0 * * * * *",
            "* -1 * * * * *",
            "* 1L * * * * *",
            "* L * * * * *",
            "* W * * * * *",
            "* LW * * * * *",
            "* * 0/0/0 * * * *",
            "* * 0/0/0,0-0 * * * *",
            "* * 24 * * * *",
            "* * -0 * * * *",
            "* * -1 * * * *",
            "* * 1L * * * *",
            "* * L * * * *",
            "* * W * * * *",
            "* * LW * * * *",
            "* * * 001 * * *",
            "* * * 1/1/1 * * *",
            "* * * 1/1/1,1-1 * * *",
            "* * * 32 * * *",
            "* * * -0 * * *",
            "* * * -1 * * *",
            "* * * 0 * * *",
            "* * * 1L * * *",
            "* * * lastL * * *",
            "* * * W * * *",
            "* * * * 001 * *",
            "* * * * 1/1/1 * *",
            "* * * * 1/1/1,1-1 * *",
            "* * * * 13 * *",
            "* * * * -0 * *",
            "* * * * -1 * *",
            "* * * * 0 * *",
            "* * * * 5L * *",
            "* * * * L/5 * *",
            "* * * * DEC-6 * *",
            "* * * * * 001 *",
            "* * * * * 5#6 *",
            "* * * * * 7 *",
            "* * * * * -0 *",
            "* * * * * 6/6/6 *",
            "* * * * * 6-6-6,6-7 *",
            "* * * * * Sun-6 *",
            "* * * * * * 12345",
            "* * * * * * 123",
            "* * * * * * 1969",
            "* * * * * * 2100",
            "* * * * * * 2030L",
            "* * * * * * 2030/2030/2030",
            "* * * * * * 2030/2030/2030,2030,2030",
            "1 18 17 3 10 * 2030 2019",
            "1 18 17 3 10 * 2030,",
            "* * * * * * 1234",
            "* * * * * * 0120-9999",
        ]
        for expression in expressions:
            result = cron.validate_cron(expression)
            self.assertFalse(result, expression)
